import numpy as np

from libs.solver import SudokuSolver

# Pytest tests for SudokuSolver class, converted from solver.py doctests.

def test_sudoku_solver() -> None:
    """
    Check if the sudoku solver is able to solve a board.
    """

    board: np.ndarray = np.array([
        [5, 3, 0, 0, 7, 0, 0, 0, 0],
        [6, 0, 0, 1, 9, 5, 0, 0, 0],
        [0, 9, 8, 0, 0, 0, 0, 6, 0],
        [8, 0, 0, 0, 6, 0, 0, 0, 3],
        [4, 0, 0, 8, 0, 3, 0, 0, 1],
        [7, 0, 0, 0, 2, 0, 0, 0, 6],
        [0, 6, 0, 0, 0, 0, 2, 8, 0],
        [0, 0, 0, 4, 1, 9, 0, 0, 5],
        [0, 0, 0, 0, 8, 0, 0, 7, 9]
    ])

    solver = SudokuSolver(board)
    assert solver.solve()

